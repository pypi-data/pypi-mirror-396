from typing import *
from pydantic import BaseModel, Field

class ProblemDetail(BaseModel):
    """
    ProblemDetail model
        RFC 7807 Problem Details for HTTP APIs.

A Problem Details object carries machine-readable details of errors in HTTP responses,
to avoid the need to define new error response formats for HTTP APIs.

Attributes:
    type: A URI reference that identifies the problem type (default: &#34;about:blank&#34;)
    title: A short, human-readable summary of the problem type
    status: The HTTP status code generated by the origin server
    detail: A human-readable explanation specific to this occurrence of the problem
    instance: A URI reference that identifies the specific occurrence of the problem
            """
    model_config = {
        "populate_by_name": True,
        "validate_assignment": True
    }
    
    type : Optional[Union[str,str]] = Field(validation_alias="type" , default = None )
    
    title : str = Field(validation_alias="title" )
    
    status : int = Field(validation_alias="status" )
    
    detail : Optional[Union[str,None]] = Field(validation_alias="detail" , default = None )
    
    instance : Optional[Union[str,None]] = Field(validation_alias="instance" , default = None )
    