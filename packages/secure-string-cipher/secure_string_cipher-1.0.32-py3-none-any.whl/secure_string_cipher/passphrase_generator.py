def generate_word_passphrase(word_count: int = 6, separator: str = "-") -> str:
    """Generate word-based passphrase using EFF diceware wordlist.

    Args:
        word_count: Number of words (minimum 4)
        separator: Word separator (default: "-")

    Returns:
        Passphrase like "mountain-tiger-ocean-basket"
    """
    if word_count < 4:
        raise ValueError("Word count must be at least 4 for security")

    return separator.join(secrets.choice(WORD_LIST) for _ in range(word_count))


def generate_alphanumeric_passphrase(
    length: int = 24, include_symbols: bool = True
) -> str:
    """Generate random alphanumeric passphrase with optional symbols.

    Args:
        length: Passphrase length (minimum 16)
        include_symbols: Include special characters

    Returns:
        Passphrase like "xK9$mP2@qL5#vR8&nB3!"
    """
    if length < 16:
        raise ValueError("Length must be at least 16 for security")

    lower_chars = string.ascii_lowercase
    upper_chars = string.ascii_uppercase
    digit_chars = string.digits
    symbol_chars = "!@#$%^&*()-_=+[]{}|;:,.<>?"

    required_sets = [lower_chars, upper_chars, digit_chars]
    if include_symbols:
        required_sets.append(symbol_chars)

    if length < len(required_sets):
        raise ValueError("Length must be at least as many as required character sets")

    chars = lower_chars + upper_chars + digit_chars
    if include_symbols:
        chars += symbol_chars

    # Ensure at least one character from each required set is present
    passphrase_chars = [secrets.choice(charset) for charset in required_sets]

    remaining = length - len(passphrase_chars)
    passphrase_chars.extend(secrets.choice(chars) for _ in range(remaining))

    secrets.SystemRandom().shuffle(passphrase_chars)
    return "".join(passphrase_chars)


def generate_mixed_passphrase(word_count: int = 4, number_count: int = 4) -> str:
    """Generate mixed passphrase combining words and numbers.

    Args:
        word_count: Number of words (minimum 3)
        number_count: Number of digits (minimum 3)

    Returns:
        Passphrase like "tiger-ocean-basket-palace-9247"
    """
    if word_count < 3:
        raise ValueError("Word count must be at least 3 for security")
    if number_count < 3:
        raise ValueError("Number count must be at least 3 for security")

    words = "-".join(secrets.choice(WORD_LIST) for _ in range(word_count))
    numbers = "".join(secrets.choice(string.digits) for _ in range(number_count))

    return f"{words}-{numbers}"


def calculate_entropy_bits(passphrase_type: str, **params) -> float:
    """Calculate passphrase entropy in bits.

    Args:
        passphrase_type: Type ('word', 'alphanumeric', 'mixed')
        **params: Type-specific parameters

    Returns:
        Entropy in bits
    """
    match passphrase_type:
        case "word":
            word_count = params.get("word_count", 6)
            return math.log2(len(WORD_LIST)) * word_count

        case "alphanumeric":
            length = params.get("length", 24)
            include_symbols = params.get("include_symbols", True)

            alphabet_size = len(string.ascii_letters) + len(string.digits)
            if include_symbols:
                alphabet_size += len("!@#$%^&*()-_=+[]{}|;:,.<>?")

            return math.log2(alphabet_size) * length

        case "mixed":
            word_count = params.get("word_count", 4)
            number_count = params.get("number_count", 4)

            return math.log2(len(WORD_LIST)) * word_count + math.log2(10) * number_count

        case _:
            return 0.0


def generate_passphrase(
    strategy: str = "word",
    word_count: int = 6,
    length: int = 24,
    include_symbols: bool = True,
    number_count: int = 4,
) -> tuple[str, float]:
    """Generate secure passphrase using specified strategy.

    Args:
        strategy: 'word', 'alphanumeric', or 'mixed'
        word_count: Number of words (for word/mixed strategies)
        length: Character length (for alphanumeric)
        include_symbols: Include symbols (for alphanumeric)
        number_count: Number of digits (for mixed strategy)

    Returns:
        (passphrase, entropy_bits)

    Raises:
        ValueError: If strategy is unknown
    """
    match strategy:
        case "word":
            passphrase = generate_word_passphrase(word_count)
            entropy = calculate_entropy_bits("word", word_count=word_count)

        case "alphanumeric":
            passphrase = generate_alphanumeric_passphrase(length, include_symbols)
            entropy = calculate_entropy_bits(
                "alphanumeric", length=length, include_symbols=include_symbols
            )

        case "mixed":
            passphrase = generate_mixed_passphrase(word_count, number_count)
            entropy = calculate_entropy_bits(
                "mixed", word_count=word_count, number_count=number_count
            )

        case _:
            raise ValueError(
                f"Unknown strategy '{strategy}'. Use 'word', 'alphanumeric', or 'mixed'"
            )

    return passphrase, entropy
