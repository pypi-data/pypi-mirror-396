    def get_pass(self, pass_name, band, try_name="T01"):
        """
        The `try_name` specifies the processing version:
        - "T01" is the standard processing, each flight individually processed with the optimal squint
        - "T10" is the D-InSAR processing, all flights processed with the same squint
        """
        master_name, inf_folder_name = self._get_default_master_and_inf_folder(pass_name, band, try_name)
        return CROPEX25Pass(self.campaign_folder, pass_name, band, master_name, try_name, inf_folder_name)

    def get_all_pass_names(self, band):
        pass_names = [pass_name for pass_name, ps_b in self._passes_bands if ps_b == band]
        return sorted(list(set(pass_names)))  # sort and de-duplicate

    def _get_default_master_and_inf_folder(self, pass_name, band, try_name):
        if pass_name in self._north_track_passes:
            inf_geometry = "INF_80"  # north
            master_name = "25cropex0505"
        elif pass_name in self._orthogonal_track_passes:
            inf_geometry = "INF_90"  # orthogonal
            master_name = "25cropex1625"
        elif band == "S":
            inf_geometry = "INF_33"  # south, track 33
            master_name = "25cropex0514"
        else:
            inf_geometry = "INF_20"  # south, track 20
            master_name = "25cropex0504"

        if try_name == "T01":
            if inf_geometry in ["INF_80", "INF_90"]:
                master_name = None  # no master for north and orthogonal geometry with t01
            # inf folder name depends on flight number, not geometry
            flight_id, pass_id = campaign_utils.get_flight_and_pass_ids(pass_name)
            triple_flights = {
                "04": "04-05-06",
                "05": "04-05-06",
                "06": "04-05-06",
                "11": "11-12-13",
                "12": "11-12-13",
                "13": "11-12-13",
                "21": "21-22",
                "22": "21-22",
                "23": "23",
            }
            if flight_id in triple_flights:
                inf_folder_name = f"INF_{triple_flights[flight_id]}"
            else:
                inf_folder_name = f"INF_{flight_id}"
            return master_name, inf_folder_name

        if try_name == "T10":
            inf_folder_name = inf_geometry
            return master_name, inf_folder_name

        raise ValueError(f"Unknown combination: {pass_name} {band} {try_name}")


