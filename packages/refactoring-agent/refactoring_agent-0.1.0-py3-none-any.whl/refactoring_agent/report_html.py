import json
import os
from datetime import datetime

def generate_html_report(issues, output_path):
    """
    Generates a standalone HTML report for the findings.
    """
    # CSS braces are escaped as {{ }} to avoid conflict with python .format()
    html_template = """
    <!DOCTYPE html>
    <html lang="en">
    <head>
        <meta charset="UTF-8">
        <meta name="viewport" content="width=device-width, initial-scale=1.0">
        <title>Refactoring Agent Report</title>
        <style>
            body {{ font-family: -apple-system, BlinkMacSystemFont, "Segoe UI", Roboto, Helvetica, Arial, sans-serif; margin: 0; padding: 20px; background: #f4f4f9; color: #333; }}
            .container {{ max-width: 1000px; margin: 0 auto; background: #fff; padding: 20px; border-radius: 8px; box-shadow: 0 2px 5px rgba(0,0,0,0.1); }}
            h1 {{ color: #2c3e50; border-bottom: 2px solid #eee; padding-bottom: 10px; }}
            .summary {{ display: flex; gap: 20px; margin-bottom: 20px; }}
            .card {{ background: #f8f9fa; padding: 15px; border-radius: 5px; flex: 1; text-align: center; border: 1px solid #e9ecef; }}
            .card h3 {{ margin: 0; font-size: 2em; color: #007bff; }}
            .card p {{ margin: 5px 0 0; color: #6c757d; }}
            table {{ width: 100%; border-collapse: collapse; margin-top: 20px; }}
            th, td {{ padding: 12px; text-align: left; border-bottom: 1px solid #ddd; }}
            th {{ background-color: #f8f9fa; }}
            tr:hover {{ background-color: #f1f1f1; }}
            .badge {{ padding: 4px 8px; border-radius: 4px; font-size: 0.85em; font-weight: bold; color: #fff; }}
            .critical {{ background-color: #dc3545; }}
            .major {{ background-color: #fd7e14; }}
            .minor {{ background-color: #ffc107; color: #333; }}
            .footer {{ margin-top: 40px; text-align: center; color: #aaa; font-size: 0.9em; }}
        </style>
    </head>
    <body>
        <div class="container">
            <h1>üõ°Ô∏è Security & Refactoring Report</h1>
            <div class="summary">
                <div class="card">
                    <h3>{total_issues}</h3>
                    <p>Total Issues</p>
                </div>
                <div class="card">
                    <h3>{critical_count}</h3>
                    <p>Critical Risks</p>
                </div>
                <div class="card">
                    <h3>{files_affected}</h3>
                    <p>Files Affected</p>
                </div>
            </div>

            <table>
                <thead>
                    <tr>
                        <th>Severity</th>
                        <th>Rule ID</th>
                        <th>Location</th>
                        <th>Message</th>
                    </tr>
                </thead>
                <tbody>
                    {table_rows}
                </tbody>
            </table>
            
            <div class="footer">
                Generated by Refactoring Agent on {date}
            </div>
        </div>
    </body>
    </html>
    """

    critical_count = sum(1 for i in issues if i.get('severity') == 'critical')
    files_affected = len(set(i['file'] for i in issues))
    
    rows = ""
    for issue in issues:
        severity = issue.get('severity', 'minor')
        # Check severity to assign badge color
        badge_class = severity if severity in ['critical', 'major', 'minor'] else 'minor'
        
        rows += f"""
        <tr>
            <td><span class="badge {badge_class}">{severity.upper()}</span></td>
            <td><code>{issue['rule_id']}</code></td>
            <td>{issue['file']}:{issue['line']}</td>
            <td>{issue['message']}</td>
        </tr>
        """

    report_content = html_template.format(
        total_issues=len(issues),
        critical_count=critical_count,
        files_affected=files_affected,
        table_rows=rows,
        date=datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    )

    with open(output_path, "w", encoding="utf-8") as f:
        f.write(report_content)
