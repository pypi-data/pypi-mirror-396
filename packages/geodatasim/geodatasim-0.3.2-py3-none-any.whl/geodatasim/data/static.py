def get_city_data(city_name: str, country: Optional[str] = None) -> Optional[Dict[str, Any]]:
    """
    Get static data for a city.

    Args:
        city_name: City name
        country: Optional country name for disambiguation

    Returns:
        City data dictionary or None if not found
    """
    city_name_lower = city_name.lower()

    # Exact match first
    for city in CITIES_DATABASE:
        if city['name'].lower() == city_name_lower:
            if country is None or city['country'].lower() == country.lower():
                return city.copy()

    # Partial match
    for city in CITIES_DATABASE:
        if city_name_lower in city['name'].lower():
            if country is None or city['country'].lower() == country.lower():
                return city.copy()

    return None


def search_city(query: str) -> List[Dict[str, Any]]:
    """
    Search for cities by name.

    Args:
        query: Search query

    Returns:
        List of matching city data dictionaries
    """
    query_lower = query.lower()
    results = []

    for city in CITIES_DATABASE:
        if query_lower in city['name'].lower() or query_lower in city['country'].lower():
            results.append(city.copy())

    return results


def get_all_cities() -> List[Dict[str, Any]]:
    """
    Get all cities in the database.

    Returns:
        List of all city data dictionaries
    """
    return [city.copy() for city in CITIES_DATABASE]


def get_cities_by_country(country: str) -> List[Dict[str, Any]]:
    """
    Get all cities for a specific country.

    Args:
        country: Country name

    Returns:
        List of city data dictionaries for that country
    """
    country_lower = country.lower()
    return [city.copy() for city in CITIES_DATABASE
            if city['country'].lower() == country_lower]
